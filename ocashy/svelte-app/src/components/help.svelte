<h1>Guide Book</h1>
<p>Ocashy is a comprehensive cashier application designed to streamline and simplify the management of your business. With its user-friendly interface and robust features, Ocashy aims to be an indispensable tool for businesses of all sizes.</p>
<h3>Features</h3>
<ul>
    <li>Inventory Search Engine</li>
    <li>Inventory Management</li>
    <li>Cashier / Reciepts Maker</li>
    <li>Warehouse Management</li>
</ul>
<h3>How to use</h3>
<p>If you want to use <b>Inventory Search Engine</b>, you have to click on Daftar Produk side menu (on the left side of your screen) and then you can simply type your product that you want to search on the search bar.</p>
